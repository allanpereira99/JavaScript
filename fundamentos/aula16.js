// Operadores logicos
const n = 1
const a = 1

//operador 'e'
if (a ==1 && n ==1){
    console.log('true')
}else{
    console.log('false')
}

//operador 'ou'
if (a ==1 || n ==1){
    console.log('true')
}else{
    console.log('false')
}

//operador 'ou exclusivo' requer que exclusivamente um seja verdadeiro ou falso

if (a ==1 ^ n ==1){
    console.log('true')
}else{
    console.log('false')
}

//operador unario 

if (a ==1 != n ==1){
    console.log('true')
}else{
    console.log('false')
}